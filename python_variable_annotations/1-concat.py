#!/usr/bin/env python3
""" a module that concatenate 2 strings"""


def concat(a: str, b: str) -> str:
    """
    a function that concatenate two strings

    Attributes:
        a(str): a string to concatenate
        b(str): a string to concatenate

    Returns:
        the concatenated strings
    """
    return (str(a + b))
